#include "libft.h"

int	ft_strcmp(char const *s1, char const *s2)
{
	size_t			i;
	unsigned char	*p1;
	unsigned char	*p2;

	p1 = (unsigned char *)s1;
	p2 = (unsigned char *)s2;
	i = 0;
	while (p1[i] == p2[i] && p1[i] && p2[i])
		i++;
	return (p1[i] - p2[i]);
}
